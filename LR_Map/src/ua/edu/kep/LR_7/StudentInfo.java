package ua.edu.kep.LR_7;

public class StudentInfo {
	   private float minimal;
	   private float average; 
	   private float maximal; 
	   
		public float getMinimal() {
			return minimal;
		}
		public void setMinimal(float min){
			minimal = min;
		}
		
		public float getAverage() {
			return average;
		}
		public void setAverage(float avg){
			average = avg;
		}
		
		public float getMaximal() {
			return maximal;
		}
		public void setMaximal(float max){
			maximal = max;
		}
}
